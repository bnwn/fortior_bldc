void UART_SendData(unsigned int T_Data)
{
	UT_DR = T_Data >> 8;
	while(!(TI==1));		//等待发送完成
	TI = 0;							//发送完成中断标志位清零
	UT_DR = T_Data;
	while(!(TI==1));		//等待发送完成
	TI = 0;							//发送完成中断标志位清零
}